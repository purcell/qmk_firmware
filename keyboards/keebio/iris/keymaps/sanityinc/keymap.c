//////////////////////////////////////////////////////////////////////////////
// Helpers
//////////////////////////////////////////////////////////////////////////////

void toggle_layer(uint16_t layer, keyrecord_t *record) {
  if (record->event.pressed) {
    layer_on(layer);
  } else {
    layer_off(layer);
  }
}

void raise_or_lower(uint16_t layer, keyrecord_t *record) {
  toggle_layer(layer, record);
  update_tri_layer(_LOWER, _RAISE, _ADJUST);
}

//////////////////////////////////////////////////////////////////////////////
// QMK user hooks
//////////////////////////////////////////////////////////////////////////////

bool process_record_user(uint16_t keycode, keyrecord_t *record) {
  switch (keycode) {
    case QWERTY:
      if (record->event.pressed) {
        set_single_persistent_default_layer(_QWERTY);
      }
      return false;
    case LOWER:
      raise_or_lower(_LOWER, record);
      return false;
    case RAISE:
      raise_or_lower(_RAISE, record);
      return false;
    case ADJUST:
      toggle_layer(_ADJUST, record);
      return false;
  }
  return true;
}

uint32_t layer_state_set_user(uint32_t state) {
  switch (biton32(state)) {
  case _LOWER: rgblight_sethsv_noeeprom_green(); break;
  case _RAISE: rgblight_sethsv_noeeprom_magenta(); break;
  case _ADJUST: rgblight_sethsv_noeeprom_yellow(); break;
  default: //  for any other layers, or the default layer
    rgblight_sethsv_noeeprom_blue(); break;
  }
  return state;
}

void keyboard_post_init_user(void) {
  // Set default led colours.
  rgblight_enable_noeeprom(); // enables RGB, without saving settings
  rgblight_mode_noeeprom(RGBLIGHT_MODE_BREATHING + 1); // sets mode to Fast breathing without saving
  rgblight_sethsv_noeeprom_blue();
}

